#include<stdio.h>
int main()
{ 	//进制转化需要代码来完成
 
	int _a=0x17;//定义16进制数，输出为10进制 
	printf("%d \n",_a);
	_a=1700000;//定义10进制数，输出为16进制 
	printf("%5d\n",_a);
	printf("%x \n",_a);
	_a=017;//_a=0017也可以，定义8进制数， 输出为10进制 
	printf("%d \n",_a);
	_a=17;//定义10进制数，输出为8进制 
	printf("%o \n",_a);
	_a=017;//定义8进制数，输出16进制 
	printf("%x \n",_a);
	_a=0x17;//定义16进制数，输出为8进制 
	printf("%o",_a); 
 } 
